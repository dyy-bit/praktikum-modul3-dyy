#include<iostream>

int main()
{
using namespace std;

cout << "Pembagi integer: 9/5 = " << 9 /5 << endl;
cout << "Pembagi floating-point: 9.0/5.0 = ";
cout << 9.0 / 5.0 << endl;
cout << "Pembagian campuran: 9.0/5 = " << 9.5 / 5 << endl;
cout << "Pembagian double: 1e7/9.0 = ";
cout << 1.e7 / 9.0 << endl;
cout << "Pembagian float: 1e7f/9.0f = ";
cout << 1e7f / 9.0f << endl;

return 0;





}
